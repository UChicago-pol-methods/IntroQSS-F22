uoc_board <- tibble(
  Y = c(650, 800, 500, 1600, 650, 900, 1200),
  X1 = c(550, 600, NA, 1300, NA, NA, 700),
  X2 = c(100, 200, NA, 300, NA, NA, 500))


uoc_board


## ------------------------------------------------------------------------------------------------------------------------------------------
uoc_board |> 
  summarize(
    mean(Y),
    mean(X1),
    mean(X2)
  )


## ------------------------------------------------------------------------------------------------------------------------------------------
uoc_board <- uoc_board |> 
  mutate(X1_lb = case_when(is.na(X1) ~ 0,
                           TRUE ~ X1),
         X1_ub = case_when(is.na(X1) ~ 500,
                           TRUE ~ X1))


## ------------------------------------------------------------------------------------------------------------------------------------------
uoc_board |> 
  summarize(
    mean(X1_lb),
    mean(X1_ub)
  )


